package Exercicios.ExSemana03;

public class Exercicio01 {
    public static void main(String[] args) {
        /*
        ✅ Exercício 1 – Contar de 1 a 10
        Use um while para imprimir os números de 1 a 10 na tela.
         */
        int contador = 1;
        while (contador <= 10) {
            System.out.println("Acrescendo + "+ contador);
            contador++;
        }
    }
}
